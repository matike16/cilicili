// 头部下拉弹窗
var popup1 = document.getElementById('popup1');
var hideTimeout1;

function showPopup1() {
  popup1.style.display = 'block';
  setTimeout(function() {
    popup1.classList.add('fade-in');
  }, 10);
}

function hidePopup1() {
  hideTimeout1 = setTimeout(function() {
    popup1.classList.remove('fade-in');
    setTimeout(function() {
      popup1.style.display = 'none';
    }, 300);
  }, 200);
}

function cancelHide1() {
  clearTimeout(hideTimeout1);
}

